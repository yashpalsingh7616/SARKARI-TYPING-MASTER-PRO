let currentLang = 'en', timer, timeLeft = 300;
let isStarted = false, passageWords = [], currentWordIndex = 0;
let errors = 0, totalCharsTyped = 0, backspaces = 0;

function showPage(pageId) {
    document.querySelectorAll('.page').forEach(p => p.style.display = 'none');
    document.getElementById(pageId).style.display = 'flex';
}

function handleLogin() {
    if(document.getElementById('user-name').value) showPage('selection-page');
    else alert("नाम भरें!");
}

function openModule(lang) {
    currentLang = lang;
    document.getElementById('module-label').innerText = lang.toUpperCase() + " Module";
    showPage('passage-page');
    const list = document.getElementById('passage-list');
    list.innerHTML = passages[lang].map(p => `
        <div style="padding:10px; border-bottom:1px solid #eee; display:flex; justify-content:space-between;">
            <span>${p.title}</span>
            <button onclick="startTyping(\`${p.text}\`)">Select</button>
        </div>
    `).join('');
}

function startCustom() {
    const text = document.getElementById('custom-text').value;
    if(text) startTyping(text);
}

function startTyping(text) {
    showPage('typing-page');
    resetGame();
    passageWords = text.trim().split(/\s+/);
    const display = document.getElementById('text-display');
    display.innerHTML = passageWords.map(w => `<span class="word-span">${w}</span>`).join(' ');
    display.children[0].classList.add('current-word');

    const area = document.getElementById('typing-area');
    if(currentLang === 'hi') { area.classList.add('hindi-font'); display.classList.add('hindi-font'); }
    else { area.classList.remove('hindi-font'); display.classList.remove('hindi-font'); }

    area.onkeydown = (e) => {
        if(!isStarted) startTimer();
        if(e.key === 'Backspace') { backspaces++; document.getElementById('live-back').innerText = backspaces; }
    };

    area.oninput = () => {
        const val = area.value;
        totalCharsTyped = (passageWords.slice(0, currentWordIndex).join(' ').length) + val.length;
        if(val.endsWith(' ')) {
            const input = val.trim();
            const span = display.children[currentWordIndex];
            if(input === passageWords[currentWordIndex]) span.className = "word-span correct";
            else { span.className = "word-span wrong"; errors++; }
            
            currentWordIndex++;
            if(currentWordIndex < passageWords.length) {
                area.value = "";
                display.children[currentWordIndex].classList.add('current-word');
                display.children[currentWordIndex].scrollIntoView({behavior:'smooth', block:'center'});
            } else { finishTest(); }
            updateStats();
        }
    };
}

function startTimer() {
    isStarted = true;
    timer = setInterval(() => {
        timeLeft--;
        let m = Math.floor(timeLeft/60), s = timeLeft%60;
        document.getElementById('timer').innerText = `Time: ${m}:${s<10?'0':''}${s}`;
        if(timeLeft <= 0) finishTest();
    }, 1000);
}

function updateStats() {
    const timeElapsed = (300 - timeLeft) / 60 || 0.1;
    const gross = Math.round((totalCharsTyped / 5) / timeElapsed);
    document.getElementById('live-wpm').innerText = gross;
    document.getElementById('live-errors').innerText = errors;
}

function finishTest() {
    clearInterval(timer);
    alert("Test Finished! Check your results.");
    location.reload();
}

function resetGame() {
    isStarted = false; timeLeft = 300; currentWordIndex = 0; errors = 0; backspaces = 0;
}

function confirmExit() { if(confirm("Back to menu?")) location.reload(); }
